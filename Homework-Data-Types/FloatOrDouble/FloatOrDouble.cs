using System;

class FloatOrDouble
{
    static void Main()
    {
        double Double1 = 34.567839023d;
        double Double2 = 8923.1234857d;
        float Float1 = 3456.091F;
        float Float2 = 12.345F;

        Console.WriteLine(Double1 + " - This is assigned to type double");
        Console.WriteLine(Double2 + " - This is assigned to type double");
        Console.WriteLine(Float1 + " - This is assigned to type float");
        Console.WriteLine(Float2 + " - This is assigned to type float");
    }
}

